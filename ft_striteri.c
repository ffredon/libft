#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;
	int				c;

	i = 0;
	c = 0;
	while (s[c])
	{
		f(i, &s[c]);
		i++;
		c++;
	}
}
